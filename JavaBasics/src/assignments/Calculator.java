package assignments;

public class Calculator {

	public void add(int a, int b) {
		System.out.println(a + b);
	}

	public void add(int a, int b, int c) {
		System.out.println(a + b + c);
	}

	public void add(int a, int b, int c, int d) {
		System.out.println(a + b + c + d);
	}

	public void subtract(int a, int b) {
		System.out.println(a - b);
	}

	public void subtract(int a, int b, int c) {
		System.out.println(a - b - c);
	}

	public void subtract(int a, int b, int c, int d) {
		System.out.println(a - b - c - d);
	}

	public void multiply(int a, int b) {
		System.out.println(a * b);
	}

	public void multiply(int a, int b, int c) {
		System.out.println(a * b * c);
	}

	public void multiply(int a, int b, int c, int d) {
		System.out.println(a * b * c * d);
	}

	public void divide(double a, double b) {
		System.out.println(a / b);
	}

	public void divide(double a, double b, double c) {
		System.out.println(a / b / c);
	}

	public void divide(double a, double b, double c, double d) {
		System.out.println(a / b / c / d);
	}
}
